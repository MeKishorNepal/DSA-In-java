package Lab8;

public class StringLength {

	public static void main(String[] args) {
		String s="The Sun rises in the east and sets in the west";
		System.out.println("length of entire string is "+s.length());
		System.out.println("length of index i is "+s.indexOf('i'));
		System.out.println("Length of index st is "+s.lastIndexOf("st"));
		

	}

}
